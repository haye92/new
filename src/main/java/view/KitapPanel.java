            k.kitapYazari = jTable1.getValueAt(seciliSatirSirasi, 2).toString();
            k.kitapSayfa = Integer.parseInt(jTable1.getValueAt(seciliSatirSirasi, 3).toString());
            k.kitapYayinevi = jTable1.getValueAt(seciliSatirSirasi, 4).toString();
            k.kitapKategori = jTable1.getValueAt(seciliSatirSirasi, 5).toString();
            
            k.guncelle(Integer.parseInt(seciliKitapID),k);
            System.out.println("Başarılı");
            tabloYenile();
