    public void tabloyuDoldur(ArrayList<Kitap> kitaplar)
    {
          
          String veriler[][]=new String[kitaplar.size()][];

          for (int i = 0; i < kitaplar.size(); i++) {     
             veriler[i]=new String[]{                                   
                kitaplar.get(i).kitapID +"",
                kitaplar.get(i).kitapAdi,
                kitaplar.get(i).kitapYazari,
                kitaplar.get(i).kitapSayfa +"",
                kitaplar.get(i).kitapYayinevi,
                kitaplar.get(i).kitapKategori                    
             };                  
         }
          String [] tabloBaslik = new String[] {"Kitap ID", "Kitap Adı", "Kitap Yazarı", "Sayfa Sayısı", "Kitap Yayınevi", "Kitap Kategori"};
          TableModel tabloModeli=new DefaultTableModel( veriler, tabloBaslik );                                                
          jTable1.setModel(tabloModeli);
     }

